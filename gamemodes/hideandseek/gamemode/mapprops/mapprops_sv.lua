function PopulateMap()
	for k,v in pairs(MapProps) do
		if game.GetMap() == k then
			table.foreach(MapProps[k].STATIC,function(key,val)
				local db = string.Explode("|",val)
				local mdl = db[1]
				local pos = util.StringToType(db[2],"Vector")
				local ang = util.StringToType(db[3],"Angle")
				local skn = (db[4] != nil) and tonumber(db[4]) or 0
				local prop = ents.Create("prop_physics")
				prop:SetModel(mdl)
				prop:SetPos(pos)
				prop:SetAngles(ang)
				prop:SetSkin(skn)
				prop:Spawn()
				prop:GetPhysicsObject():EnableMotion(false)
			end)
			table.foreach(MapProps[k].PHYS,function(key,val)
				local db = string.Explode("|",val)
				local mdl = db[1]
				local pos = util.StringToType(db[2],"Vector")
				local ang = util.StringToType(db[3],"Angle")
				local skn = (db[4] != nil) and tonumber(db[4]) or 0
				local slp = (db[5] != nil) and true or false
				local prop = ents.Create("prop_physics")
				prop:SetModel(mdl)
				prop:SetPos(pos)
				prop:SetAngles(ang)
				prop:SetSkin(skn)
				prop:Spawn()
				if slp then prop:GetPhysicsObject():Sleep() else prop:GetPhysicsObject():Wake() end
			end)
			table.foreach(MapProps[k].LIGHT,function(key,val)
				local db = string.Explode("|",val)
				local pos = util.StringToType(db[1],"Vector")
				local mode = (db[2] == nil) and 0 or tonumber(db[2])
				local prop = ents.Create("prop_physics")
				prop:SetModel("models/props_junk/popcan01a.mdl")
				prop:SetPos(pos)
				prop:SetAngles(Angle(0,0,0))
				prop:SetRenderMode(1)
				prop:SetColor(Color(255,255,255,0))
				prop:SetSkin(mode)
				prop:SetMaterial("models/effects/comball_glow2") --client takes it from here
				prop:Spawn()
				prop:GetPhysicsObject():EnableMotion(false)
				prop:SetSolid(0)
			end)
		end
	end
end